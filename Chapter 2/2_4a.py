# Addition
print(8 + 10)

# Subtraction
print(8 - 10)

# Multiplication
print(8 * 2)

# Exponentiation
print(8 ** 2)

# Division - produces a float
print(64/8)

# Exact division - produces a float
print(65/8)

# Quotient - produces an integer (leaves off the fractional part)
print(65//8)

# Remainder or modolus
print(65 % 8)

# For multi-line expressions, usa a \
print(3 + 4 * \
      2 ** 5)

# Mixed-mode arithmetic involves integers
# and float-point numbers
print(3.14 * 3 ** 2)
