# Integers - include 0, the positive whole numbers,
# and the negative whole numbers

myInt = 0
myInt = 42
myInt = -7


# Floating-Point Numbers - consists of a whole number,
# decimal point, and a fractional part.
# Can be written using either ordinary decimal notation
# or scientific notation

myFloat = 3.14
myFloat = -0.001
myFloat = 2.5e6     # 2.5 times 10 to the 6th power


# Character sets - Python uses the ASCII character set,
# which includes letters, digits, punctuation marks,
# and special characters such as the space character
# ASCII character set maps to a set of integers (see Table 2-5)

print(ord("a"))
print(ord("A"))
print(chr(65))
