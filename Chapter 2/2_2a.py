# A string literal is a sequence of characters enclosed in
# either single quotes (') or double quotes (")
print('Hello, World!')
print("Hello, World!")

# To print a single quote inside a string
print("I'm using a single quote in this string")
      
# Or use an escape sequence
print('I\'m using a single quote in this string')

# To print several lines, use three consecutive quotation marks
print("""This very long sentence extends
all the way to the next line.""")

# To print several lines, you can also use the new line (\n) character
print("This very long sentence extends\ball the way to the next line")